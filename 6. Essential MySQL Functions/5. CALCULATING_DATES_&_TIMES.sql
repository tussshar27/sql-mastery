SELECT DATE_ADD(NOW(), INTERVAL 1 YEAR);	-- 2021-12-30 10:49:13
	-- GIVES next year value bcoz of interval of 1, same as for month, day by replacing YEAR in query.
SELECT DATE_SUB(NOW(), INTERVAL 1 YEAR);	-- 2019-12-30 10:51:31 -- GIVES PREVIOUS DATE WITH CURRENT TIME
SELECT DATE_SUB(NOW(), INTERVAL 3 YEAR);	-- 2017-12-30 10:52:31
SELECT DATEDIFF('2020-01-19', '2020-01-01');	-- 18	-- IT WILL GIVE DIFFERENCE ONLY IN DATES IRRESPECCTIVE OF ANYTHING MONTH, YEAR, ETC.
SELECT DATEDIFF('2020-01-01', '2020-01-19');	-- -18
SELECT TIME_TO_SEC('09:02') - TIME_TO_SEC('09:00');	-- 120	-- GIVES THE SECONDS IN DIFFERENCE.